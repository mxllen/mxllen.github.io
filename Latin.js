const CASE = [0, 1, 2, 3, 4];

function declension(noun, number) {
	if (noun.gender === 'feminine') {
		feminineDeclension(noun, number);
	} else if (noun.gender === 'masculine') {
		masculineDeclension(noun, number);
	} else if (noun.gender === 'u') {
		uDeclension(noun, number);
	} else if (noun.gender === 'neuterMixed') {
		neuterMixedDeclension(noun, number);
	} else if (noun.gender === 'neuter') {
		neuterDeclension(noun, number);
	} else if (noun.gender === 'mixed' || 'mixedNoI') {
		mixedDeclension(noun, number);
	}
}

function masculineDeclension(noun, number) {
	let casus = 0;
	if (noun.gender === 'masculine') {
		if (number === 'singular') {
		for (let casus of CASE) {
			console.log(`${noun.stem}${masculine.singular[casus]}`);
			casus += 1;
			}
		} else if (number === 'plural') {
			for (let casus of CASE) {
			console.log(`${noun.stem}${masculine.plural[casus]}`);
			casus += 1;
			}
		}
	}
}

